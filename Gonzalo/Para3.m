function P=Para3(D)
T0=24.582602450649823;
A(1,1) = -D(:,4)'*D(:,4);
A(1,2) = D(:,4)'*(D(:,2)-T0);
A(1,3) = D(:,4)'*D(:,5);
A(2,1) = -D(:,4)'*(D(:,2)-T0);
A(2,2) = (D(:,2)-T0)'*(D(:,2)-T0);
A(2,3) = (D(:,2)-T0)'*D(:,5);
A(3,1) = -A(1,3);
A(3,2) = A(2,3);
A(3,3) = D(:,5)'*D(:,5);

b(1,1) = (D(:,3)'*D(:,4));
b(2,1) = (D(:,3)'*(D(:,2)-T0));
b(3,1) = (D(:,3)'*D(:,5));

P=A\b;
end